
public class if_else {
    public static void main(String[] args) {
        int num = 5;

        if (num == 10) {
            System.out.println("Number equal to 10");
        } else if (num != 10) {
            System.out.println("Number not equal to 10");
        } else if (num > 10) {
            System.out.println("Number greater than 10");
        } else if (num >= 10) {
            System.out.println("Number greater than or equal to 10");
        } else if (num <= 10) {
            System.out.println("YES");
        }

    }

}
