public class apple {
    public static void main(String[] args) {
        System.out.println("Jhon said, \"I am hungry\" "); // \" use for print "

        System.out.println("D:\\Github\\Java "); // use \\ for print \

        // \n :- new line tag
        System.out.println("Jhon said, \nI am hungry ");

        // \r :- overwrite tag
        System.out.println("Jhon said, \rI am hungry ");

        // \t :- tabspace tag
        System.out.println("Jhon said, \tI am hungry ");

    }
}
